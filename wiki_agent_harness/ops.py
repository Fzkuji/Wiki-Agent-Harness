def stats(vault_root: Path) -> dict[str, Any]:
    by_template: dict[str, int] = {}
    n = 0
    for p in pages.iter_pages(vault_root):
        try:
            meta = slots.read_meta_file(p)
        except Exception:
            continue
        t = str(meta.get("template") or "?")
        by_template[t] = by_template.get(t, 0) + 1
        n += 1

    db = store.index_db_path(store.state_dir(vault_root))
    try:
        idx = _idx.stats(db)
    except Exception:
        idx = {}

    return {
        "pages": n,
        "by_template": by_template,
        "fts_rows": idx.get("pages", 0),
        "last_reindex": idx.get("last_reindex"),
        "vault_root": str(vault_root),
    }
