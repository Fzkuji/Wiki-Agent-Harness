#: Folder is flagged for split when it has more direct content pages than this.
FOLDER_SPLIT_THRESHOLD = 7


def lint(vault_root: Path, *, renderer: Renderer) -> str:
    """Report page-level issues + folder housekeeping signals.

    Folder housekeeping checks flag conditions that should trigger an agent
    tidy pass: folders too large to grok at a glance, folders whose README
    has no human-written description, and folders that are empty.
    """
    pages_list = list(pages.iter_pages(vault_root))
    if not pages_list:
        return "# Wiki lint report\n\n(empty vault — nothing to lint)"

    known_templates = set(renderer.template_names())
    template_slots = {t.name: set(t.slots) for t in renderer.list_templates()}

    missing_meta: list[str] = []
    unknown_template: list[tuple[str, str]] = []
    bad_slots: list[tuple[str, str]] = []
    broken_links: list[tuple[str, str]] = []
    empty_pages: list[str] = []

    for p in pages_list:
        rel = str(p.relative_to(vault_root))
        try:
            html = p.read_text(encoding="utf-8")
        except OSError:
            continue
        meta = slots.read_meta(html)
        if not meta:
            missing_meta.append(rel)
            continue

        tmpl = meta.get("template")
        if not tmpl or tmpl not in known_templates:
            unknown_template.append((rel, str(tmpl or "<missing>")))
            continue

        declared = template_slots.get(str(tmpl), set())
        actual = set(slots.list_slots(html))
        unexpected = actual - declared
        for s in unexpected:
            bad_slots.append((rel, s))

        if not slots.slot_text(html).strip():
            empty_pages.append(rel)

        for href in _extract_hrefs(html):
            target = _resolve_href(p, href, vault_root)
            if target is None:
                continue
            if not target.exists():
                broken_links.append((rel, href))

    # ── Reachability check ───────────────────────────────────────────
    # A page is "buried" if the only inbound reference is the auto-
    # generated PAGES list in its own folder's README (i.e. nobody
    # writes about it anywhere). Such pages are reachable only by
    # someone who already knows the folder structure — they have no
    # contextual entry point. Rule: every page must be referenced from
    # (a) its parent folder README's description slot, OR (b) an
    # ancestor folder's README description slot, OR (c) some other
    # content page's body/slots. Auto page-list doesn't count.
    meaningful_inbound: dict[Path, list[str]] = {p.resolve(): [] for p in pages_list}

    # Sources of outbound hrefs to consider: content pages (their full
    # body) PLUS folder READMEs (only their slot content — not the
    # auto-generated PAGES list, which is template chrome, not an
    # intentional reference).
    href_sources: list[tuple[Path, str]] = []
    for p in pages_list:
        try:
            href_sources.append((p, p.read_text(encoding="utf-8")))
        except OSError:
            continue
    for folder in _iter_folders(vault_root):
        readme = folder / store.FOLDER_INDEX
        if not readme.exists():
            continue
        try:
            html = readme.read_text(encoding="utf-8")
        except OSError:
            continue
        slot_text = "\n".join(
            slots.read_slot(html, sid) or "" for sid in slots.list_slots(html)
        )
        href_sources.append((readme, slot_text))

    for src_path, scan_text in href_sources:
        rel_src = str(src_path.relative_to(vault_root))
        for href in _extract_hrefs(scan_text):
            target = _resolve_href(src_path, href, vault_root)
            if target is None:
                continue
            target = target.resolve()
            if target == src_path.resolve():
                continue  # self-link
            if target in meaningful_inbound:
                meaningful_inbound[target].append(rel_src)

    buried: list[str] = []
    for p in pages_list:
        if not meaningful_inbound[p.resolve()]:
            buried.append(str(p.relative_to(vault_root)))

    # ── Folder housekeeping ───────────────────────────────────────────
    crowded: list[tuple[str, int]] = []
    missing_description: list[str] = []
    empty_folders: list[str] = []
    missing_readme: list[str] = []

    for folder in _iter_folders(vault_root):
        rel_folder = "(root)" if folder == vault_root else str(folder.relative_to(vault_root))
        direct_pages = [
            e for e in folder.iterdir()
            if e.suffix == store.PAGE_SUFFIX and e.name != store.FOLDER_INDEX
        ]
        direct_subfolders = [
            e for e in folder.iterdir()
            if e.is_dir() and not e.name.startswith(".")
        ]
        if len(direct_pages) > FOLDER_SPLIT_THRESHOLD:
            crowded.append((rel_folder, len(direct_pages)))
        if not direct_pages and not direct_subfolders:
            empty_folders.append(rel_folder)

        readme = folder / store.FOLDER_INDEX
        if not readme.exists():
            missing_readme.append(rel_folder)
            continue
        try:
            desc = (slots.read_slot_file(readme, "description") or "").strip()
        except Exception:
            desc = ""
        if not desc:
            missing_description.append(rel_folder)

    out: list[str] = [
        "# Wiki lint report",
        "",
        f"Pages:                              {len(pages_list)}",
        f"Missing meta block:                 {len(missing_meta)}",
        f"Unknown template:                   {len(unknown_template)}",
        f"Slots not declared by template:     {len(bad_slots)}",
        f"Pages with no slot content:         {len(empty_pages)}",
        f"Broken local <a href> links:        {len(broken_links)}",
        f"Folders missing README.html:        {len(missing_readme)}",
        f"Folders without description slot:   {len(missing_description)}",
        f"Folders with >{FOLDER_SPLIT_THRESHOLD} direct pages (split?):  {len(crowded)}",
        f"Empty folders:                      {len(empty_folders)}",
        f"Buried pages (no inbound ref):      {len(buried)}",
        "",
    ]

    def _section(title: str, rows: list[str]) -> None:
        if not rows:
            return
        out.append(f"## {title}")
        out.extend(f"- {r}" for r in rows[:30])
        if len(rows) > 30:
            out.append(f"- ... and {len(rows) - 30} more")
        out.append("")

    _section("Missing meta", missing_meta)
    _section("Unknown template",
             [f"`{p}` → `{t}`" for p, t in unknown_template])
    _section("Unexpected slots",
             [f"`{p}` → slot `{s}`" for p, s in bad_slots])
    _section("Empty pages", empty_pages)
    _section("Broken links",
             [f"`{p}` → `{h}`" for p, h in broken_links])
    _section("Folders missing README.html", missing_readme)
    _section("Folders without description slot", missing_description)
    _section("Crowded folders (consider splitting)",
             [f"`{f}` — {n} direct pages" for f, n in crowded])
    _section("Empty folders", empty_folders)
    _section(
        "Buried pages — link them from an ancestor README description, "
        "feature in a callout, OR move to where they're actually used. "
        "Being auto-listed in the parent's PAGES section doesn't count: "
        "the reader has to drill in blind to find them.",
        buried,
    )

    return "\n".join(out).rstrip()


def _iter_folders(vault_root: Path):
    yield vault_root
    for p in sorted(vault_root.rglob("*")):
        if not p.is_dir():
            continue
        rel = p.relative_to(vault_root)
        if any(part in store.SKIP_DIRS or part.startswith(".") for part in rel.parts):
            continue
        yield p


_SCRIPT_RE = __import__("re").compile(r"<script\b[^>]*>.*?</script>", __import__("re").DOTALL | __import__("re").IGNORECASE)
_STYLE_RE = __import__("re").compile(r"<style\b[^>]*>.*?</style>", __import__("re").DOTALL | __import__("re").IGNORECASE)
_HREF_RE = __import__("re").compile(r'<a\b[^>]*\shref="([^"]+)"', __import__("re").IGNORECASE)


def _extract_hrefs(html: str) -> list[str]:
    # Strip <script> and <style> so we don't catch JS-template hrefs.
    body = _SCRIPT_RE.sub("", html)
    body = _STYLE_RE.sub("", body)
    return [m.group(1) for m in _HREF_RE.finditer(body)]


def _resolve_href(page_path: Path, href: str, vault_root: Path) -> Path | None:
    """Resolve a local href relative to ``page_path``. Returns None for external."""
    if href.startswith(("http://", "https://", "mailto:", "#", "javascript:")):
        return None
    href_clean = href.split("#", 1)[0].split("?", 1)[0]
    if not href_clean:
        return None
    target = (page_path.parent / href_clean).resolve()
    try:
        target.relative_to(vault_root.resolve())
    except ValueError:
        return None
    return target


