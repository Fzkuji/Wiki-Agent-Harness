def git_commit(message: str, *, vault_root: Path) -> dict[str, Any]:
    if not (vault_root / ".git").exists():
        return {"ok": False, "error": "vault is not a git repo"}
    try:
        subprocess.run(
            ["git", "-C", str(vault_root), "add", "-A"],
            check=True, capture_output=True, timeout=15,
        )
        status = subprocess.run(
            ["git", "-C", str(vault_root), "status", "--porcelain"],
            check=True, capture_output=True, timeout=15, text=True,
        ).stdout.strip()
        if not status:
            return {"ok": True, "committed": False}
        subprocess.run(
            ["git", "-C", str(vault_root), "commit", "-m", message,
             "--author", "WikiAgentHarness <memory@wiki-agent.local>"],
            check=True, capture_output=True, timeout=15,
        )
        h = subprocess.run(
            ["git", "-C", str(vault_root), "rev-parse", "--short", "HEAD"],
            check=True, capture_output=True, timeout=15, text=True,
        ).stdout.strip()
    except subprocess.CalledProcessError as e:
        return {"ok": False, "error": (e.stderr or b"").decode() or str(e)}
    except Exception as e:
        return {"ok": False, "error": str(e)}
    return {"ok": True, "committed": True, "hash": h}


def git_touched_pages(vault_root: Path) -> tuple[list[Path], list[Path]]:
    """Return (all_touched, newly_created) content pages since last commit."""
    if not (vault_root / ".git").exists():
        return [], []
    try:
        out = subprocess.run(
            ["git", "-C", str(vault_root), "status", "--porcelain"],
            check=True, capture_output=True, timeout=15, text=True,
        ).stdout
    except Exception:
        return [], []
    touched: list[Path] = []
    created: list[Path] = []
    for line in out.splitlines():
        if len(line) < 4:
            continue
        st = line[:2]
        rel = line[3:].strip().strip('"')
        if " -> " in rel:
            rel = rel.split(" -> ", 1)[1]
        if not rel.endswith(store.PAGE_SUFFIX):
            continue
        p = vault_root / rel
        if p.name == store.FOLDER_INDEX:
            continue
        if not p.exists():
            continue
        touched.append(p)
        if st.strip() == "??":
            created.append(p)
    return touched, created


