# ---------------------------------------------------------------------------
# New page
# ---------------------------------------------------------------------------


def new_page(
    name: str,
    *,
    template: str,
    meta: dict[str, Any] | None = None,
    folder: str | Path | None = None,
    renderer: Renderer,
    vault_root: Path,
) -> Path:
    """Create a fresh page from a template. Returns the new page path.

    Page lives at ``<vault_root>/<folder>/<name>.html``. The folder is
    created if missing. The folder index is regenerated.
    """
    if folder is None:
        folder_path = vault_root
    elif isinstance(folder, Path):
        folder_path = folder
    else:
        folder_path = vault_root / folder
    folder_path.mkdir(parents=True, exist_ok=True)

    safe_name = name if name.endswith(store.PAGE_SUFFIX) else f"{name}{store.PAGE_SUFFIX}"
    page_path = folder_path / safe_name
    if page_path.exists():
        raise FileExistsError(f"page already exists: {page_path}")

    page_meta = dict(meta or {})
    page_meta.setdefault("title", Path(safe_name).stem.replace("-", " ").replace("_", " "))

    materialize_new_page(
        page_path, template=template, meta=page_meta,
        renderer=renderer, vault_root=vault_root,
    )

    _rebuild_chain_to_root(folder_path, renderer=renderer, vault_root=vault_root)
    db = store.index_db_path(store.state_dir(vault_root))
    _idx.update_page(page_path, vault_root, db)
    return page_path


_TAG_RE = __import__("re").compile(r"<[^>]+>")
_WS_RE = __import__("re").compile(r"\s+")


def _card_snippet(html_or_text: str, max_len: int = 180) -> str:
    """Make a short, plain-text snippet for use in a folder card.

    Strips all HTML, collapses whitespace, takes the first sentence (up to
    the first period/Chinese period/newline) capped at ``max_len`` chars.
    """
    if not html_or_text:
        return ""
    text = _TAG_RE.sub(" ", html_or_text)
    text = _WS_RE.sub(" ", text).strip()
    if not text:
        return ""
    for sep in ("\n", "。", ". ", "！", "? ", "? "):
        idx = text.find(sep)
        if 0 < idx < max_len:
            text = text[: idx + len(sep)]
            break
    if len(text) > max_len:
        text = text[: max_len].rstrip() + "…"
    return text.strip()


def _rebuild_chain_to_root(
    folder: Path, *, renderer: Renderer, vault_root: Path,
) -> None:
    """Rebuild folder index for ``folder`` and every ancestor up to (and
    including) ``vault_root``. Child lists change up the chain whenever a
    new subfolder is created.
    """
    folder = folder.resolve()
    root = vault_root.resolve()
    current = folder
    while True:
        rebuild_folder_index(current, renderer=renderer, vault_root=vault_root)
        if current == root:
            break
        if root not in current.parents:
            break
        current = current.parent


# ---------------------------------------------------------------------------
# Rerender (apply current template to an existing page, preserving slots)
# ---------------------------------------------------------------------------


def rerender_page(
    path: Path, *, renderer: Renderer, vault_root: Path,
) -> bool:
    """Re-render an existing page from its current template, keeping slots.

    Returns True if the page was re-rendered, False if it was skipped
    (no meta block, unknown template, or read error). Use this after
    editing templates to refresh existing pages' shells without losing
    body content.
    """
    try:
        html = path.read_text(encoding="utf-8")
    except OSError:
        return False
    meta = slots.read_meta(html)
    if not meta:
        return False
    template = str(meta.get("template") or "")
    if not template or renderer.find_template(template) is None:
        return False
    if template == "folder":
        # folder/README.html is regenerated by rebuild_folder_index.
        return False

    saved: dict[str, str] = {}
    for sid in slots.list_slots(html):
        val = slots.read_slot(html, sid)
        if val is not None:
            saved[sid] = val

    new_html = renderer.render_new_page(
        template, meta, depth=depth_of(path, vault_root),
    )
    for sid, val in saved.items():
        try:
            new_html = slots.write_slot(new_html, sid, val)
        except KeyError:
            continue  # slot no longer declared by template
    path.write_text(new_html, encoding="utf-8")
    return True


def rerender_all_pages(
    vault_root: Path, *, renderer: Renderer,
) -> int:
    """Re-render every harness-managed content page. Returns count."""
    n = 0
    for p in pages.iter_pages(vault_root):
        try:
            if rerender_page(p, renderer=renderer, vault_root=vault_root):
                n += 1
        except Exception as e:
            logger.warning("rerender failed for %s: %s", p, e)
    return n


# ---------------------------------------------------------------------------
# Folder index regeneration
# ---------------------------------------------------------------------------


def rebuild_folder_index(
    folder: Path,
    *,
    renderer: Renderer,
    vault_root: Path,
) -> Path:
    """Regenerate ``<folder>/README.html`` from the folder's current contents.

    Preserves any slot content (notably ``description``) already written into
    the previous README — only the auto-managed parts (child list, updated
    stamp) are refreshed.

    For subfolder cards, the ``note`` shown is taken from the child folder
    README's ``description`` slot when present, falling back to ``meta.description``.
    """
    folder = folder.resolve()
    children: list[dict[str, Any]] = []

    for entry in sorted(folder.iterdir(), key=lambda p: (not p.is_dir(), p.name.lower())):
        if entry.name.startswith("."):
            continue
        if entry.is_dir():
            sub_readme = entry / store.FOLDER_INDEX
            sub_meta: dict[str, Any] = {}
            sub_desc = ""
            if sub_readme.exists():
                try:
                    sub_meta = slots.read_meta_file(sub_readme)
                    sub_desc = (slots.read_slot_file(sub_readme, "description") or "").strip()
                except Exception:
                    pass
            note = _card_snippet(sub_desc or sub_meta.get("description") or "")
            # Count direct content pages + subfolders so the card can show
            # "5 pages · 2 folders" at a glance.
            n_pages = 0
            n_subfolders = 0
            try:
                for sub in entry.iterdir():
                    if sub.name.startswith(".") or sub.name.startswith("_"):
                        continue
                    if sub.is_dir():
                        n_subfolders += 1
                    elif sub.suffix == store.PAGE_SUFFIX and sub.name != store.FOLDER_INDEX:
                        n_pages += 1
            except OSError:
                pass
            children.append({
                "kind": "folder",
                "title": sub_meta.get("title") or entry.name,
                "href": f"{entry.name}/{store.FOLDER_INDEX}",
                "note": note,
                "n_pages": n_pages,
                "n_subfolders": n_subfolders,
            })
        elif entry.suffix == store.PAGE_SUFFIX and entry.name != store.FOLDER_INDEX:
            try:
                meta = slots.read_meta_file(entry)
            except Exception:
                meta = {}
            children.append({
                "kind": "page",
                "title": meta.get("title") or entry.stem,
                "href": entry.name,
                "template": meta.get("template") or "",
                "updated": meta.get("updated") or "",
            })

    # Surface a visible warning when the folder has too many direct content
    # pages — a clear signal that it should be split into subfolders. The
    # threshold (10) matches the human "more than 10 files = unstructured
    # dumping" heuristic.
    direct_page_count = sum(1 for c in children if c.get("kind") == "page")
    crowded_warning = direct_page_count > 10

    readme = folder / store.FOLDER_INDEX
    folder_meta: dict[str, Any] = {}
    preserved_slots: dict[str, str] = {}
    if readme.exists():
        try:
            old_html = readme.read_text(encoding="utf-8")
            folder_meta = slots.read_meta(old_html)
            for sid in slots.list_slots(old_html):
                val = slots.read_slot(old_html, sid)
                if val and val.strip():
                    preserved_slots[sid] = val
        except Exception:
            pass
    folder_meta.setdefault("title", folder.name if folder != vault_root else "wiki")

    html = renderer.render_folder_index(
        folder_meta, children, depth=depth_of(readme, vault_root),
        crowded_warning=crowded_warning,
        direct_page_count=direct_page_count,
    )
    for sid, content in preserved_slots.items():
        try:
            html = slots.write_slot(html, sid, content)
        except KeyError:
            # Slot no longer declared by the template; drop it.
            continue

    readme.write_text(html, encoding="utf-8")

    # Also write a plain-text README.md sibling so GitHub / IDE folder
    # browsers auto-render the index in markdown view.
    try:
        md_path = readme.with_suffix(".md")
        md_path.write_text(
            _render_folder_index_md(folder_meta, children, vault_root, folder),
            encoding="utf-8",
        )
    except Exception as e:
        logger.warning("README.md sibling write failed for %s: %s", folder, e)

    return readme


def _render_folder_index_md(
    meta: dict[str, Any],
    children: list[dict[str, Any]],
    vault_root: Path,
    folder: Path,
) -> str:
    """Plain-text markdown projection of a folder index.

    GitHub auto-renders ``README.md`` when viewing a folder, so this gives
    visitors the same navigation in plain markdown.
    """
    out: list[str] = []
    title = str(meta.get("title") or folder.name or "Index")
    out.append(f"# {title}")
    out.append("")

    desc = _card_snippet(meta.get("description") or "", max_len=400)
    if desc:
        out.append(desc)
        out.append("")

    folders = [c for c in children if c.get("kind") == "folder"]
    pages_ = [c for c in children if c.get("kind") == "page"]

    from urllib.parse import quote
    if folders:
        out.append("## Subfolders")
        out.append("")
        for c in folders:
            href = c["href"].rsplit("/", 1)[0] + "/"
            line = f"- [**{c['title']}**]({quote(href, safe='/')})"
            if c.get("note"):
                line += f" — {c['note']}"
            out.append(line)
        out.append("")

    if pages_:
        out.append("## Pages")
        out.append("")
        out.append("| Title | Template | Updated |")
        out.append("|---|---|---|")
        for c in pages_:
            tmpl = c.get("template") or ""
            upd = c.get("updated") or ""
            out.append(f"| [{c['title']}]({quote(c['href'], safe='/')}) | `{tmpl}` | {upd} |")
        out.append("")

    if not folders and not pages_:
        out.append("_(empty folder)_")
        out.append("")

    out.append("---")
    out.append("")
    out.append(
        f"_This file is auto-generated. The richly rendered version is "
        f"[`README.html`](./README.html); the human-edited description lives "
        f"in its `description` slot._"
    )
    return "\n".join(out)


def rebuild_all_folder_indexes(
    vault_root: Path, *, renderer: Renderer,
) -> int:
    """Regenerate every folder index. Returns count rebuilt."""
    n = 0
    rebuild_folder_index(vault_root, renderer=renderer, vault_root=vault_root)
    n += 1
    for sub in vault_root.rglob("*"):
        if not sub.is_dir():
            continue
        rel = sub.relative_to(vault_root)
        if any(part in store.SKIP_DIRS or part.startswith(".") for part in rel.parts):
            continue
        rebuild_folder_index(sub, renderer=renderer, vault_root=vault_root)
        n += 1
    return n


